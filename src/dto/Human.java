package dto;

public class Human {
	public String number;
	public String name;
	public String age;
	public String height;
	public String position;

	public Human() {}

	public Human(String number, String name, String age, String height,String position) {
		super();
		this.number = number;
		this.name = name;
		this.age = age;
		this.height = height;
		this.position = position;
	}
}
